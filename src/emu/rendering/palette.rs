pub struct UIPalette<'a> {
    pub name: &'a str,
    pub palette: RawPalette,
}
impl<'a> UIPalette<'a> {
    pub const fn new(name: &'a str, colors: [[u8; 3]; 4]) -> Self {
        Self { name, palette: RawPalette::new(colors) }
    }
}

#[derive(Clone)]
pub struct RawPalette {
    white:      [u8; 3],
    light_gray: [u8; 3],
    dark_gray:  [u8; 3],
    black:      [u8; 3],
}
impl RawPalette {
    pub const fn new(colors: [[u8; 3]; 4]) -> Self {
        Self {
            white:      colors[0],
            light_gray: colors[1],
            dark_gray:  colors[2],
            black:      colors[3],
        }
    }
    pub fn match_shade(&self, value: RawShade) -> [u8; 3] {
        match value {
            RawShade::Zero => self.white,
            RawShade::One =>  self.light_gray,
            RawShade::Two =>  self.dark_gray,
            _ =>               self.black,
        }
    }
    #[allow(dead_code)]
    pub fn reverse_match_pixel(&self, value: [u8; 3]) -> RawShade {
        if value == self.white              { RawShade::Zero  }
        else if value == self.light_gray    { RawShade::One   }
        else if value == self.dark_gray     { RawShade::Two   }
        else                                { RawShade::Three }
    }
    pub fn match_pixel_utf8(value: RawShade) -> &'static str {
        match value {
            RawShade::Zero => "░",
            RawShade::One  => "▒",
            RawShade::Two  => "▓",
            _ =>              "█",
        }
    }
}